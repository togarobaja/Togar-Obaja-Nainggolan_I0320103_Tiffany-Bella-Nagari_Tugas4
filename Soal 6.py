# Keluaran Operator Bitwise

print('Pengecekan output operator bitwise')
a = 4  # 4 = 0100 dalam biner
b = 11  # 11 = 1011 dalam biner

# a. 4 | 11
c = a | b
print("Hasil dari 4 | 11:", c)
# b. 4 >> 11
c = a >> b
print("Hasil dari 4 >> 11:", c)
# c. 4 ^ 11
c = a ^ b
print("Hasil dari 4 ^ 11:", c)
# d. ~4
c = ~a
print("Hasil dari ~4:", c)
# d. 11 & 4
c = b & a
print("Hasil dari 11 & 4 adalah :", c)
